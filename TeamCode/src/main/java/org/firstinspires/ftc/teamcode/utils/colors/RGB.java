package org.firstinspires.ftc.teamcode.utils.colors;

public class RGB {
    private int r, g, b;
    public RGB(int _r, int _g, int _b) {
        r = _r;
        g = _g;
        b = _b;
    }

    public void setRGB(int _r, int _g, int _b) {
        r = _r;
        g = _g;
        b = _b;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}
